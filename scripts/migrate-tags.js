// 语言 tag → languages 字段自然映射（不需要在 tags 里重复）
const LANG_TAGS = new Set([
  '中文', '英语', '日语', '韩语', '德语', '法语', '俄语', '意大利语', '西班牙语'
]);

const files = fs.readdirSync(BASE).filter(f => f.endsWith('.yaml'));

for (const file of files) {
  const filePath = path.join(BASE, file);
  const raw = fs.readFileSync(filePath, 'utf8');
  let content = raw;

  let changed = 0;

  for (const [id, newTags] of Object.entries(NEW_TAGS)) {
    // 匹配 tags 行：`tags: [旧标签列表]`，支持换行和逗号空格
    const re = new RegExp(
      `(\\b${id}\\b[\\s\\S]*?tags:\\s*)\\[[^\\]]*\\]`,
      'm'
    );
    const match = content.match(re);
    if (match) {
      const newLine = match[1] + '[' + newTags.join(', ') + ']';
      content = content.replace(match[0], newLine);
      changed++;
    } else {
      console.warn(`  ⚠️ 未找到 ${id} 的 tags 行`);
    }
  }

  if (changed > 0) {
    fs.writeFileSync(filePath, content, 'utf8');
    console.log(`✅ ${file}: ${changed} voices updated`);
  }
}

console.log('\nDone. Run `node voices/build.js` to rebuild.');
